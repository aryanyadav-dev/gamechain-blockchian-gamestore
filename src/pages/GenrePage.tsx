export function GenrePage() {
  const { genre } = useParams();
  const genreKey = genre as keyof typeof GENRE_GAMES;
  const games = GENRE_GAMES[genreKey] || [];

  const genreName = genre
    ?.split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');

  return (
    <div className="max-w-7xl mx-auto px-4 py-8">
      <div className="mb-8">
        <h1 className="text-3xl font-bold text-white mb-2">{genreName} Games</h1>
        <p className="text-gray-400">Discover the best {genreName} games</p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
        {games.map((game) => (
          <GameCard key={game.id} game={game} />
        ))}
      </div>

      {games.length === 0 && (
        <div className="text-center py-12">
          <p className="text-gray-400">No games found in this genre.</p>
        </div>
      )}
    </div>
  );
}